import UIKit

struct AppTintColors
{
    static var primaryTintColor: UIColor
    {
        get
        {
            return UIColor.whiteColor()
        }
    }
    
    static var backgroundTintColor: UIColor
    {
        get
        {
            return UIColor(colorLiteralRed: 88/255, green: 21/255, blue: 48/255, alpha: 1.0)
        }
    }
}